/*Напишіть програму на Kotlin, яка запрошує у користувача вік.
Якщо вік більше 18 років, то вивести на екран повідомлення
"Користувач повнолітній", інакше - "Неповнолітній".*/

fun main(){
    print("Вкажіть ваш вік: ")
    val age:Int = readln().toInt()
    if (age >= 18) println("Користувач повнолітній.")
    else println("Користувач неповнолітній.")
}