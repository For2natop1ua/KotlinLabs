package Lab11

fun main() {
    // Створюємо екземпляр класу Line
    val line = Line(100f, 100f, 0f, 1f, 50f)

    // Викликаємо методи класу Line

    // Показати фігуру на екрані
    line.draw()
    // Повернути фігуру на 45 градусів
    line.rotate(45f)
    // Пересунути фігуру на вектор (50, 50)
    line.translate(50f, 50f)
    // Вивести площу фігури
    println("Площа фігури: ${line.area()}")
    // Зробити фігуру невидною
    line.erase()
}